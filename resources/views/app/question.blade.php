@section('script')
<script>
    const accordionComponent = document.querySelector(".accordions");
    const accordions = document.querySelectorAll(".accordion");

    const states = {
      default: {
        desktop: 120,
        mobile: 80,
      },
      active: {
        desktop: 148,
        mobile: 104,
      },
      inactive: {
        desktop: 80,
        mobile: 50,
      },
    };

    const desktopBp = window.matchMedia("(min-width: 1024px)");

    const getInactiveAccordions = () => {
      return [...accordions].filter((accordion) => {
        return !accordion.classList.contains("is-active");
      });
    };

    const closeAccordion = (accordion) => {
      const header = accordion.querySelector(".accordion__header");
      const content = accordion.querySelector(".accordion__content");
      accordion.classList.remove("is-active");
      header.setAttribute("aria-expanded", false);
      content.setAttribute("aria-hidden", true);
    };

    const resetAccordionStates = () => {
      accordionComponent.classList.remove("has-active-accordion");
      accordions.forEach((accordion) => {
        closeAccordion(accordion);
      });
      gsap.to(accordions, {
        height: desktopBp.matches
          ? states.default.desktop
          : states.default.mobile,
        duration: 2,
        ease: desktopBp.matches
          ? Elastic.easeOut.config(1, 0.8)
          : Elastic.easeOut.config(0.8, 0.95),
      });
    };

    desktopBp.addEventListener("change", () => {
      const activeAccordion = document.querySelector(".accordion.is-active");

      if (activeAccordion) {
        return;
      }

      gsap.to(accordions, {
        height: desktopBp.matches
          ? states.default.desktop
          : states.default.mobile,
        duration: 2,
        ease: desktopBp.matches
          ? Elastic.easeOut.config(1, 0.8)
          : Elastic.easeOut.config(0.8, 0.95),
      });
    });

    const expandAccordion = (accordion, header, content) => {
      if (accordion.classList.contains("is-active")) {
        resetAccordionStates();
        return;
      }
      accordions.forEach((accordion) => {
        closeAccordion(accordion);
      });
      accordionComponent.classList.add("has-active-accordion");
      accordion.classList.add("is-active");
      header.setAttribute("aria-expanded", true);
      content.setAttribute("aria-hidden", false);
      gsap.to(getInactiveAccordions(), {
        height: desktopBp.matches
          ? states.inactive.desktop
          : states.inactive.mobile,
        duration: 1.8,
        ease: desktopBp.matches
          ? Elastic.easeOut.config(1, 0.7)
          : Elastic.easeOut.config(0.8, 0.7),
      });
      gsap.to(accordion, {
        height:
          content.scrollHeight +
          (desktopBp.matches ? states.active.desktop : states.active.mobile),
        duration: 2.2,
        ease: desktopBp.matches
          ? Elastic.easeOut.config(1, 0.4)
          : Elastic.easeOut.config(0.6, 0.8),
      });
    };

    accordions.forEach((accordion) => {
      const header = accordion.querySelector(".accordion__header");
      const content = accordion.querySelector(".accordion__content");

      header.addEventListener("click", () =>
        expandAccordion(accordion, header, content)
      );
    });

    const updateAccordionSizing = () => {
      const activeAccordion = document.querySelector(".accordion.is-active");

      if (!activeAccordion) {
        return;
      }
      const content = activeAccordion.querySelector(".accordion__content");
      const currentHeight = parseInt(activeAccordion.style.height);
      if (currentHeight === content.scrollHeight + 148) {
        return;
      }

      activeAccordion.style.height = `${content.scrollHeight + 148}px`;
    };

    let timeout = false;
    window.addEventListener("resize", function () {
      clearTimeout(timeout);
      timeout = setTimeout(() => {
        updateAccordionSizing();
      }, 100);
    });
  </script>
@endsection